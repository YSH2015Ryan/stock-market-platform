    async def analyze_sentiment(self, text: str) -> Dict:
        """Analyze sentiment of news or text"""
        # Simple rule-based sentiment for demo
        # In production, use transformer models or OpenAI API

        positive_words = ["gain", "surge", "rally", "profit", "growth", "upgrade", "beat", "exceed"]
        negative_words = ["loss", "drop", "decline", "fall", "miss", "downgrade", "concern", "risk"]

        text_lower = text.lower()

        positive_count = sum(1 for word in positive_words if word in text_lower)
        negative_count = sum(1 for word in negative_words if word in text_lower)

        if positive_count > negative_count:
            sentiment = "positive"
            score = min(0.5 + (positive_count * 0.1), 1.0)
        elif negative_count > positive_count:
            sentiment = "negative"
            score = max(-0.5 - (negative_count * 0.1), -1.0)
        else:
            sentiment = "neutral"
            score = 0.0

        return {
            "sentiment": sentiment,
            "score": score,
            "confidence": abs(score)
        }

