    def _generate_market_summary(self, indices: List) -> str:
        """Generate market summary from indices"""
        if not indices:
            return "Market data not available."

        summary_parts = []
        for index in indices:
            direction = "up" if index.change and index.change > 0 else "down"
            summary_parts.append(
                f"- {index.name} ({index.symbol}): {index.value:.2f} ({direction} {abs(index.change_percent or 0):.2f}%)"
            )

        return "\n".join(summary_parts)

